
CREATE DATABASE PUNE_UNIVERSITY 

USE PUNE_UNIVERSITY

CREATE TABLE COLLEGE_TABLE 
(
COLLEGE_ID INT PRIMARY KEY NOT NULL ,
COLLEGE_NAME VARCHAR (100) NOT NULL ,
COLLEGE_AREA VARCHAR (100) NOT NULL 
)

CREATE TABLE DEPARTMENT_TABLE 
(
DEPT_ID INT PRIMARY KEY NOT NULL , 
DEPT_NAME VARCHAR(100) NOT NULL ,
DEPT_FACULTY VARCHAR (100) NOT NULL ,
COLLEGE_ID INT FOREIGN KEY REFERENCES COLLEGE_TABLE
)


CREATE TABLE PROFESSOR_TABLE 
(
PROFESSOR_ID INT PRIMARY KEY ,
PROFESSOR_NAME VARCHAR (100),
PROFESSOR_SUBJECT VARCHAR (100),
DEPT_ID INT FOREIGN KEY REFERENCES DEPARTMENT_TABLE
)


CREATE TABLE STUDENT_TABLE 
(
STUDENT_ID INT PRIMARY KEY ,
STUDENT_NAME VARCHAR (100),
STUDENT_STREAM VARCHAR (100),
PROFESSOR_ID INT FOREIGN KEY REFERENCES PROFESSOR_TABLE 
)


